let x=10, y=20;

//Arithmetic Operators 

console.log(x+y);
console.log(y-x);
console.log(x*y);
console.log(y/x);
console.log(y%x);
console.log(5**2);

x++;
console.log(x);
++x;
console.log(x);
y--;
